public class Foo {
    private final String bar;

    public Foo(String bar) {
        this.bar = bar;
    }

    public String getBar() {
        return bar;
    }
}

// Допустим, что метод getFoo может вернуть объект Foo или null
Foo buz = getFoo();

int length = 0;
if (buzz != null && buzz.getBar() != null) {
    length = buz.getBar().length;
}
